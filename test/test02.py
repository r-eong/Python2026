# 기본 출력, 줄바꿈 : \n
print("안녕하세요\n파이썬 학습을 환영합니다")

# 탭 \t 을 이용한 간격 정렬
print("\n--- 수강생 명단 ---")
print("이름\t전공\t상태 ")
print("김철수\t컴공\t열공중 ")
print("이영희\t디자인\t작업중 ")

# 따옴표 출력 \", \'
print("\n교수님이 말씀하셨습니다. \"이스케이프 문자는 아주 유용해요\"")
print('It\'s a very important lesson.')

# end 옵션을 이용한 이어쓰기
print("\n오늘의", end=" ")
print("학습", end=" ")
print("성공")